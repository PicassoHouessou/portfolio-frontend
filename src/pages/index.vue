const formatPeriod=(date1?:string, date2?:string ):string =>{
  if (date1 && date2){
    const year1 = new Date(date1).getFullYear()
    const year2 = new Date(date2).getFullYear()
    return `${year1} - ${year2}`;
  }
  return "";
}





